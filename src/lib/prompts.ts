export type Lang = 'zh' | 'ja'

export function buildQuestionPrompt(jobRole: string, experience?: string, lang: Lang = 'zh'): string {
  const expText = experience ? `候選者の経験年数は${experience}年です。` : '経験年数は不明です。'
  const questionLang = lang === 'ja'
    ? '質問は日本語で、自然なビジネス敬語を使い、80文字以内'
    : '質問は中国語で、80文字以内'
  return `あなたは日本のIT企業で10年以上採用面接を担当してきたシニアマネージャーです。
対象ポジション：${jobRole}
${expText}

以下の条件で行動面接（STAR形式）の質問を1問だけ生成してください：

条件：
- 日本の職場文化（チームワーク・責任感・改善意識）を重視
- 実際の業務シーンに基づいた具体的な状況設定
- 候補者の問題解決能力・コミュニケーション力が測れる内容
- ${questionLang}
- 「〜について教えてください」「〜した経験はありますか」形式

質問のみを出力し、説明や前置きは一切不要。`
}

